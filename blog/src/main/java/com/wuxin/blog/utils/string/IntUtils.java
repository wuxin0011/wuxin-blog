public class IntUtils {

    /**
     * 比较结果
     * @param i 参数
     * @return  true false
     */
    public static boolean is(Integer i) {
        return i == 1;
    }

    /**
     * 比较结果
     * @param i 参数
     * @return  true false
     */
    public static boolean isNot(Integer i) {
        return i != 1;
    }
}
